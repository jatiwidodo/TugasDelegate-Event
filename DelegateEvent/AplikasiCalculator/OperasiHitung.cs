using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace AplikasiCalculator
{
    class OperasiHitung
    {
        // method
        public int Penjumlahan(int a, int b)
        {
            return a + b;
        }

        public int Pengurangan(int a, int b)
        {
            return a - b;
        }

        public int Perkalian(int a, int b)
        {
            return a * b;
        }

        public int Pembagian(int a, int b)
        {
            return a / b;
        }
    }
}
